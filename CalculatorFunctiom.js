function addition (x,y)
{
    var Result = x + y;
    document.write("Sum= " + Result + "<br>");
}


function Substraction (x,y)
{
    var Result = x - y;
    document.write("Sub= " + Result + "<br>");
}

function Division (x,y)
{
    var Result = x / y;
    document.write("Div= " + Result + "<br>");
}

addition(10,20);
Substraction(20,5);
Division(20,5);   //function call

